function calculateDifference(first, second, third) {
    const sum = first + second;
    const difference = sum - third;
    return difference;
}

// Примеры использования:
console.log(calculateDifference(23, 6, 10));  // 19
console.log(calculateDifference(1, 17, 30));  // -12
console.log(calculateDifference(42, 58, 100)); // 0